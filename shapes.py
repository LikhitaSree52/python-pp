def square(side):
    area=side*side
    return area

def rectangle(length,breadth):
    area=length*breadth
    return area

def triangle(base,height):
    area=(base*height)*0.5
    return area

def circle(radius):
    area=3.14*radius*radius
    return area

def rhombus(diagonal1,diagonal2):
    area=(diagonal1*diagonal2)*0.5
    return area 

